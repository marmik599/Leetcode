//User function Template for C++
class Solution
{
public:
    int trailingZeroes(int n)
    {
        int ans=0;
        for(int i=5;(n/i)>0;i=i*5)
        {
            ans+=(n/i);
        }
        return ans;
    }
};

// { Driver Code Starts.
int main()
{
    int t;
    cin >> t;
    while (t--)
    {
        int N;
        cin >> N;
        Solution ob;
        int ans  = ob.trailingZeroes(N);
        cout<<ans<<endl;
    }
    return 0;
}  // } Driver Code Ends